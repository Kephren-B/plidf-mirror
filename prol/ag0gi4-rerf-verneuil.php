<p> <em>L'Hay-les-Roses</em> - <em>Verneuil-l'Étang</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_f.php">ligne F</a> du RER.</p>
<p>Cette branche correspond à la réouverture de la ligne de la Bastille entre Boissy-Saint-Léger et Verneuil-l'Etang. Elle est complémentaire de l'extension du <a href="8dl0i2-rera-brie-comte-robert.php">RER A à Brie-Comte-Robert</a>.</p>
<p>En correspondance avec la <a href="..\ligne\tg_sm.php">tangentielle Seine-et-Marne</a> à <a href="..\gare\16ciehk-verneuil-letang.php">Verneuil l'Étang</a>, le RER A à <a href="..\gare\ikd7ic-villecresnes-hippodrome.php">Villecresnes - Hippodrome</a>, le métro 8 à la <a href="..\gare\8dbhik-la-plage-bleue.php">Valenton - La Plage Bleue</a>, les RER C et D à <a href="..\gare\73i6ej-choisy-le-roi-gare.php">Choisy-le-Roi</a> et <a href="..\gare\g3ei4a-villeneuve-triage.php">Villeneuve-Triage</a>. Cette branche du RER F sera un nouvel axe pour relier le sud de la Seine-et-Marne au reste de l'Île-de-France.</p>
<p>Sa position lui donne un potentiel pour en faire la dorsale d'une ville nouvelle autour de Brie-Comte-Robert organisée sur un schéma comparable à Marne-la-Vallée.</p>
