<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_4.php">ligne 4</a> du métro.</p>
<p>L'extension de la ligna 4 du métro à Montmorency est une proposition d'extension de la ligne 4 au nord pour mieux desservir les Hauts-de-Seine et le sud du Val-d'Oise. Elle permettrait ainsi de relier Montmorency, Deuil-la-Barre, Épinay-sur-Seine, Villeneuve-la-Garenne et Saint-Ouen à la gare du Nord et au centre de Paris.</p>
<p style="display:none">La traversée de la Seine entre Épinay et Villeneuve-la-Garenne pourra être construite en surface et combinée avec une passerelle piétonne pour connecter le parc des Chanteraines avec le parc de l'Ile-Saint-Denis.</p>
