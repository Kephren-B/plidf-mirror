<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_g.php">ligne G</a> du RER.</p>
<p>Cette branche du RER G a vocation à améliorer la desserte de l'extrême-est de la Seine-Saint-Denis qui dispose pour le moment d'un accès médiocre aux transports ferrés. En correspondance avec les tangentielles <a href="..\ligne\tg_vo.php">Val-d'Oise</a> et <a href="..\ligne\tg_sm.php">Seine-et-Marne</a> à <a href="..\gare\18b0hdf-claye-souilly-est.php">Claye-Souilly</a>, elle ouvrira un nouvel itinéraire pour se rendre à l'<a href="..\gare\15jd1k8-cdg3.php">aéroport de Roissy</a> depuis le sud de la Seine-Saint-denis ou l'est du Val-de-Marne (via la <a href="..\ligne\tg_vdm.php">tangentielle Val-de-Marne</a> et une correspondance à <a href="..\gare\135h1mk-plateau-davron.php">Plateau d'Avron</a> ou <a href="..\gare\nj71ai-le-chenay-gagny.php">le Chénay - Gagny</a>).</p>
