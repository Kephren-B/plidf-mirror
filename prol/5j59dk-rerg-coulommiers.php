<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_g.php">ligne G</a> du RER.</p>
<p>La branche Coulommiers du RER G vise plusieurs objectifs. </p>
<ul class="normal-ul">
<li>Relier <a href="..\commune\Montreuil.php">Montreuil</a> au réseau de RER.</li>
<li>Être une alternative à l'A3, l'A4 et l'A86</li>
<li>Soulager le RER A entre la gare de Lyon et Noisy-le-Grand</li>
<li> Mieux connecter la gare de <a href="..\gare\13cj413-mlv-chessy.php">Marne-la-Vallée - Chessy TGV</a> à la Seine-Saint-Denis et à la Seine-et-Marne</li>
<li>Amener le RER à Coulommiers tout en réduisant le temps de trajet entre cette ville et Paris.</li>
</ul>
<p></p>
<p>L'infrastructure construite serait partagée avec le <a href="gcckfl-transilienp-la-ferte-gaucher.php">transilien P en direction de la Ferté-Gaucher</a> à partir de Vaires-Torcy et avec la <a href="..\ligne\tg_sm.php">tangentielle Seine-et-Marne</a> entre Chessy et Dammartin-sur-Tigeaux. Une zone de <a href="..\infos\rer_autoroute.php">correspondance RER-autoroute (A4)</a> pourra être envisagée à Bailly-Romainvilliers.</p>
<p></p>
