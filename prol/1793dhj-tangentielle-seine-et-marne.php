<p> <em>Persan-Beaumont</em> - <em>Melun</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une <a href="..\ligne\tg_sm.php">nouvelle tangentielle</a> pour le réseau Île-de-France</p>
<p>La tangentielle Seine-et-Marne serait un nouvel axe de transports pour se déplacer en grande banlieue à l'est de l'Île-de-France sans passer par Paris. Elle relierait Persan-Beaumont à l'aéroport de Roissy, Mitry-Mory, Claye-Souilly, Chessy-TGV, Verneuil-l'Étang et Melun.</p>
<p>Cette tangentielle comporte beaucoup de lignes nouvelles ou réouvertes. Partant de Melun, elle ferait le tour de l'agglomération melunaise par l'ouest sur un tracé partagé avec le tram-train de Melun. Le tracé longerait ensuite la nationale 36 jusqu'à Verneuil-l'Étang, gare de correspondance. Puis sur l'itinéraire de l'ancienne ligne de de Verneuil à Marles-en-Brie et la ligne de Gretz à Sézanne ou il atteint Mortcerf et le triangle de Dammartin.</p>
<p>En reprenant l'infrastruture partagée avec le RER G on atteindrait Chessy-TGV puis on remonterait vers le nord en direction du pôle de Claye-Souilly via la base de loisirs de Jablines. De Claye-Souilly la ligne continuerait vers le Nord en direction Compans et de la gare aéroport Charles-de-Gaulle 3. Ensuite le tracé longerait la Francilienne pour atteindre Montsoult - Maffliers et continurait sur la ligne d'Épinay au Tréport vers la vallée de l'Oise et Persan-Beaumont, le terminus.</p>
<p>En connectant Melun à Chessy et à l'aéroport de Roissy, elle répondrait à un besoin de transports pour les habitants de la Seine-et-Marne.</p>
