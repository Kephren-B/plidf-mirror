<p id="description"> </p>
<p id="line-subtitle">Une <a href="..\ligne\tg_vdm.php">nouvelle tangentielle</a> pour le réseau Île-de-France</p>
<p>La tangentielle Val-de-Marne est une proposition de réouverture au service voyageurs de la ligne de grande ceinture de Paris. Au départ de Chelles - Gournay, cette tangentielle relierait le Val-de-Marne à Massy-Palaiseau. Le tracé utiliserait la ligne de grande ceinture complémentaire. Il passerait par la branche ouest du triangle de Gagny puis desservirait Neuilly, Bry, Champigny et Chennevières-sur-Marne. Des correspondances avec le RER A seraient possible à Bry-sur-Marne et à Sucy-Bonneuil. Le tracé continuerait ensuite vers Valenton, Orly-Ville et Pont de Rungis.</p>
<p>À Pont de Rungis, un remaniement de la desserte serait effectué: les trains de la tangentielle continueraient vers Massy-Palaiseau par Chemin d'Antony tandis que les trains du RER C <a href="11d9i70-rerc-remaniement-orly.php">passeraient par Orly TGV</a> en partageant les l'infrastructures du <a href="82636i-rerf-tronc-commun.php">tronc commun du RER F</a> entre Pont de Rungis et Chilly-Mazarin.</p>
