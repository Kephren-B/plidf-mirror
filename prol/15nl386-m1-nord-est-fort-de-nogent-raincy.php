<h1 id="top"> <img class="line-logo line-logo-network line-logo-network-m" height="25" src="..\medias\logos\logo_m.svg" title="Métro" width="25"/><a class="line-link" href="..\ligne\m_1.php"><img class="line-logo line-logo-line line-logo-line-m-1" height="25" src="..\medias\logos\m_1.svg" title="Métro 1" width="25"/></a> M1 Nord Est - Le Raincy </h1>
<p> <em>Fort de Nogent</em> - <em>Chelles - Mont Chalats</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_1.php">ligne 1</a> du métro.</p>
<p>Ce prolongement globalement parallèle à la branche principale (<a href="jbab92-m1-est-vincennes-chelles.php">M1 Est</a>) vise à améliorer la desserte communale dans <a href="..\commune\Fontenay-sous-Bois.php">Fontenay-sous-Bois</a> en particulier simplifier les déplacement entre la zone de <a href="..\gare\11e4f0i-fontenay-sous-bois-gare.php">Fontenay-sous-Bois RER</a> et <a href="..\gare\13an1ff-val-de-fontenay.php">Val de Fontenay</a>, tant pour les travailleurs que pour les lycéens. Il permettra d'améliorer les connexions entre le centre de la Seine-Saint-Denis et le pôle de Val de Fontenay.</p>
<p>Enfin ce prolongement en correspondance avec les <a href="..\ligne\rer_a.php">RER A</a>, <a href="..\ligne\rer_e.php">E</a> et <a href="..\ligne\rer_g.php">G</a> serait un bon moyen de désenclaver le plateau de <a href="..\commune\Clichy-sous-Bois.php">Clichy-sous-Bois</a> - <a href="..\commune\Montfermeil.php">Montfermeil</a>.</p>
