<p> <em>Orly TGV</em> - <em>Étampes</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_f.php">ligne F</a> du RER.</p>
<p>La création du RER F au sud correspond à la résurrection l'Arpajonnais, une ancienne ligne de chemin de fer qui reliait Paris à Arpajon le long de la nationale 20. Cette section du RER F permettra de reporter une partie du trafic automobile de cette route nationale vers les transports en commun. Et ainsi soulager la nationale 20 mais également l'autoroute A6 entre Paris et Chilly-Mazarin.</p>
<p>Le RER F sera aussi un moyen pour les voyageurs de se rendre à la gare d'Orly TGV et l'aéroport d'Orly en venant du sud. En correspondance avec le RER G, le RER F permettra aux étudiants de rejoindre le pôle universitaire d'Orsay.</p>
