<p> <em>Paris</em> - <em>Malesherbes</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\tr_r.php">ligne r</a> du transilien.</p>
<p>Ce projet correspond à l'intégration de la desserte RER D du sud de la Ferté-Alais au transilien R remanié dans le cadre de la construction du RER G.</p>
<p>Le transilien utiliserait la ligne Marolles-en-Hurepoix - Ballancourt construite pour le RER G. pour passer de la ligne de Paris à Orléans à la ligne de Villeneuve-Saint-Georges à Malesherbes. Le temps de trajet entre Paris et Malesherbes serait diminué d'au moins une demi-heure.</p>
