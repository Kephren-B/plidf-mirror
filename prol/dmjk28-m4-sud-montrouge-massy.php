<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_4.php">ligne 4</a> du métro.</p>
<p>Le prolongement du métro 4 au sud vise à améliorer les connexions entre la banlieue sud et Paris. Le tracé, globalement parallèle à l'ex RN-20 et à l'autoroute A6 permettra un report du trafic depuis la route vers le métro.</p>
<p>En correspondance avec le <a href="cj89ca-rerb-tronc-commun.php">RER B</a> à <a href="..\gare\9cjfc5-porte-dorleans.php">Porte d'Orleans</a> et <a href="..\gare\hknhbk-bagneux-gare.php">Bagneux</a> et avec le <a href="82636i-rerf-tronc-commun.php">RER F</a> à porte d'Orléans et <a href="..\gare\hh3g39-lhay-les-roses-aqueduc.php">L'Haÿ-les-Roses-Aqueduc</a>, cette extension contribuera à améliorer la connexité du réseau de métro au sud de Paris.</p>
