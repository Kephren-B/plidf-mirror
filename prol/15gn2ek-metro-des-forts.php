<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\m_forts.php">ligne forts</a> du métro.</p>
<p>Le métro des forts serait une nouvelle ligne servant à relier entre elles les communes de la Seine-Saint-Denis limitrophes de Paris. Par sa position, il represente une alternative aux autoroutes A86, A186, A3 et au Boulevard périphérique.</p>
<p>Ce métro serait un VAL plutot qu'un métro à grand gabarit pour une meilleure insertion. La section entre <a href="..\gare\fm5bkn-saint-maur-creteil.php">Saint-Maur-Creteil</a> et <a href="..\gare\hhb1cb-nogent-bords-de-marne.php">Nogent-Bords-de-Marne</a> sera éventuellement une section en viaduc.</p>
