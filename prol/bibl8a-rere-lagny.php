<h1 id="top"> <img class="line-logo line-logo-network line-logo-network-rer" height="25" src="..\medias\logos\logo_rer.svg" title="RER" width="25"/><a href="..\ligne\rer_e.php"><img class="line-logo line-logo-line line-logo-line-rer-e" height="25" src="..\medias\logos\rer_E.svg" title="RER E" width="25"/></a> RER E Lagny </h1>
<p> <em>Villiers-sur-Marne</em> - <em>Lagny-sur-Marne</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\rer_e.php">ligne E</a> du RER.</p>
<p>Une nouvelle branche du RER E serait créée entre <a href="..\commune\Villiers-sur-Marne.php">Villiers-sur-Marne</a> et <a href="..\commune\Lagny-sur-Marne.php">Lagny-sur-Marne</a> pour offrir une alternative au <a href="..\ligne\rer_a.php">RER A</a> en cours de saturation entre <a href="..\commune\Fontenay-sous-Bois.php">Fontenay-sous-Bois</a> et <a href="..\commune\Torcy.php">Torcy</a> et une alternative à l'autoroute A4 pour rejoindre Paris.</p>
<p>Cette extension offre la possibilité de desservir la <a href="..\gare\7cn3gc-val-de-torcy.php">base de loisir de Torcy</a>, le nouveau pôle de correspondances de <a href="..\gare\19aj0n7-noisy-champs.php">Noisy-Champs</a> et de créer une <a href="..\infos\rer_autoroute.php">correspondance RER-autoroute</a> (A4) au niveau du <a href="..\gare\g5k20m-les-richardets.php">quartier des Richardets</a> à <a href="..\commune\Noisy-le-Grand.php">Noisy-le-Grand</a>.</p>
<p>La section Villiers-sur-Marne - Noisiel pourra avoir une infrastructure partagée avec le métro 15.</p>
