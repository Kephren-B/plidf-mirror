<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\m_sud.php">ligne sud</a> du métro.</p>
<p>La ligne métro sud est un projet de ligne de métro proposé par le Plan Ile-de-France pour mieux mailler le réseau de métro existant dans le sud de Paris et desservir des zones actuellement mal desservie par les transports ferrés en banlieue.</p>
<p> À l'est, le but est de desservir <a href="..\commune\Chennevieres-sur-Marne.php">Chennevières-sur-Marne</a> et <a href="..\commune\Ormesson.php">Ormesson</a> tout en favorisant un report du trafic depuis la nationale 4 vers le métro. Entre <a href="..\gare\c0k6mi-joinville-le-pont.php">Joinville-le-Pont</a> et <a href="..\gare\cje8gf-gare-de-lyon.php">Gare de Lyon</a> le métro serait une alternative au <a href="..\ligne\rer_a.php">RER A</a>. La station Cuvier permettrait de desservir le pôle universitaire de Jussieu et intéressera les étudiants arrivés à Paris par la gare de Lyon. La section gare de Lyon - Trocadéro servira à soulager les lignes <a href="..\ligne\m_1.php">1</a>, <a href="..\ligne\m_8.php">8</a> et <a href="..\ligne\m_9.php">9</a> tout en améliorant la desserte des 6e et 7e arrondissements de Paris.</p>
<p>La liaison Trocadéro - Le Chesnay vise à un report du trafic automobile depuis l'autoroute A13 vers le métro tout en desservant le sud de <a href="..\commune\Rueil-Malmaison.php">Rueil-Malmaison</a>. La ligne métro sud offrirait également un nouvel itinéraire pour relier les Hauts-de-Seine et la ville nouvelle de <a href="..\pole\hjn6fn-saint-quentin-en-yvelines.php">Saint-Quentin-en-Yvelines</a> en remplacement du <a href="15kad4a-metro 18.php">projet de ligne 18 du métro</a> actuellement envisagé dans le <a href="..\infos\gpe.php">Grand Paris Express</a>.</p>
<p>Les stations <a href="..\gare\52kbjk-ecole-militaire.php">École militaire</a>, <a href="..\gare\dj4hkk-quai-branly.php">Quai Branly</a> et <a href="..\gare\i239e0-trocadero.php">Trocadéro</a> seront dimensionnées pour recevoir les foules qui se déplacent pour les évènements du Champs de Mars, en particulier le 14 juillet.</p>
