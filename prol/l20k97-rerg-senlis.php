<h1 id="top"> <img class="line-logo line-logo-network line-logo-network-rer" height="25" src="..\medias\logos\logo_rer.svg" title="RER" width="25"/><a href="..\ligne\rer_g.php"><img class="line-logo line-logo-line line-logo-line-rer-g" height="25" src="..\medias\logos\rer_G.svg" title="RER G" width="25"/></a> RER G Senlis </h1>
<p> <em>CDG 3</em> - <em>Senlis</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_g.php">ligne G</a> du RER.</p>
<p>Cette branche du RER G plus ou moins parallèle à l'autoroute A3 puis l'A1 vise à un report du trafic routier vers le RER. Elle reprendrait le tracé de la voie mère qui reliait le triage du Bourget aux usines d'Aulnay-sous-Bois entre le Blanc-Mesnil et Parinor, puis une infrastruture commune avec le RER B jusqu'à la nouvelle gare RER - TER de <a href="..\gare\15jd1k8-cdg3.php">Roissy - Charles-de-Gaulle 3</a>. Au dela du Mesnil-Amelot, le tracé suivrait plus ou moins la <a href="https://fr.wikipedia.org/wiki/Ligne d'Aulnay-sous-Bois à Verberie">ligne inachevée d'Aulnay-Sous-Bois à Verberie</a> en passant par Vémar, Saint-Witz et Plailly où deux gares seraient implantées dont une pour desservir le parc Astérix.</p>
<p>Un raccordement entre Survilliers et Saint-Witz permettrait à des TER en provenance d'Amiens via Creil de desservir la gare CDG3 (projet de liaison Roissy - Picardie).</p>
<p>Des parkings de <a href="..\infos\rer_autoroute.php">correspondance RER - Autoroute</a> pourront être implantés à Garonor (A1, A3), Roissy (A1, francilienne) ou Survilliers (A1).</p>
