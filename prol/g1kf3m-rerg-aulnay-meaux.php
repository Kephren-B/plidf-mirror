<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_g.php">ligne G</a> du RER.</p>
<p>Cette branche du RER G parallèle à la nationale 3 servira à améliorer les connexions entre Meaux et la Seine-Saint-Denis, cette extension favorisera un report du trafic automobile vers le RER. Cette branche offrira à quelques communes desservies actuellement par le RER B un complément de desserte pour atteindre plus rapidement l'est et le sud de Paris.</p>
<p>On pourra envisager une permutation des services avec le RER B:<br/>Le RER B serait terminus Meaux et le RER G terminus Mitry-Claye ou Damartin-Juilly-Saint-Mard.</p>
<p>La section Claye-Gressy - Meaux sera partagée avec la <a href="..\ligne\tg_vo.php">tangentielle Val-d'Oise</a> et les TER en provenance de l'est et à destination de l'aéroport de Roissy.</p>
