<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\m_nord.php">ligne nord</a> du métro.</p>
<p>Le métro nord est un projet de ligne de métro qui permettra de concrétiser des idées qui étaient envisagées depuis longtemps.</p>
<p>Dans la partie nord-ouest de la ligne, le tracé permettra de desservir le <a href="..\gare\j1c55j-port-de-gennevilliers.php">port de Gennevilliers</a> en venant du nord ou du sud, offrir une correspondance avec la <a href="..\ligne\tg_ssd.php">tangentielle Seine-Saint-Denis</a> à <a href="..\gare\e0426d-argenteuil-viaducs.php">Argenteuil - Viaducs</a>, le RER C à <a href="..\gare\gkl5ik-saint-gratien.php">Saint-Gratien</a> et le <a href="..\ligne\tr_h.php">transilien H</a> à <a href="..\gare\ei12ac-champs-de-course-deghien.php">Champ de Course d'Enghien</a>.</p>
<p>Dans Paris, il reprendra la branche Clichy du <a href="..\ligne\m_13.php">métro 13</a> qui sera détachée du tronc commun et permettra de soulager la <a href="..\ligne\m_2.php">ligne 2</a> entre place de Clichy et Barbès-Rochechouard tout en fournissant une correspondance vers la <a href="..\gare\98gf86-gare-du-nord.php">gare du Nord</a>. Ensuite il reprendra le trace fusionné des lignes 7 bis et 3 bis de <a href="..\gare\cdi465-louis-blanc.php">Louis Blanc</a> à <a href="..\gare\nefkm5-pelleport.php">Pelleport</a> via <a href="..\gare\14hjj8n-porte-des-lilas.php">Porte des Lilas</a>.</p>
<p>Le métro nord sortira de Paris par la <a href="..\gare\e1d1h7-porte-de-bagnolet.php">porte de Bagnolet</a> pour connecter <a href="..\commune\Montreuil.php">Montreuil</a> à la station de <a href="..\gare\531in1-berault-vincennes.php">Vincennes RER</a>, traversera le bois de Vincennes pour desservir l'<a href="..\gare\10a73b9-maisons-alfort-ecole-veterinaire.php">école véterinaire de Maisons-Alfort</a> et la <a href="..\gare\192hm6i-maisons-alfort-alfortville.php">gare de Maisons-Alfort - Alfortville</a>. Le tracé repartira ensuite vers l'est pour suivre la route nationale 19 entre l'<a href="..\gare\heb0en-eglise-de-creteil.php">Église de Créteil</a> et <a href="..\gare\13e27ee-boissy-saint-leger-gare.php">Boissy-Saint-Leger</a>.</p>
