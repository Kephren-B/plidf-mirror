<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_1.php">ligne 1</a> du métro.</p>
<p>Ce prolongement du métro 1 vise à relier la Défense à Nanterre, Rueil-Malmaisons et au delà. En correspondance avec les <a href="..\ligne\m_2.php">lignes 2</a> et <a href="..\ligne\m_15.php">15</a> à <a href="..\gare\1439m0j-nanterre-la-boule.php">Nanterre - La Boule</a>, la ligne 10 à Rueil-Malmaison et le transilien L à Louveciennes, cette extension sera utile pour tous les déplacements vers et autour de la Défense.</p>
