<h1 id="top"> <img height="25" src="..\medias\logos\logo_rer.svg" title="RER" width="25"/><a href="..\ligne\rer_f.php"><img height="25" src="..\medias\logos\rer_F.svg" title="RER F" width="25"/></a> RER F Tronc commun </h1>
<p> <em>Argenteuil</em> - <em>Orly TGV</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_f.php">ligne F</a> du RER.</p>
<p>Le nouveau RER F vise à créer un nouvel axe de transport rapide entre le nord et le sud de Paris.</p>
<p>Le tronc commun démarrerait à <a href="..\gare\140b09h-argenteuil-gare.php">Argenteuil</a> par le regroupement de la <a href="hdi8f5-rerf-persan.php">branche Persan</a> avec les branches <a href="ibln4j-rerf-pontoise.php">Boissy-l'Aillerie</a> et <a href="dnb2ke-rerf-mantes.php">Mantes-la-Jolie</a> Le tracé vers Paris utiliserait les infrastructures de la ligne de Paris-Saint-Lazare à Mantes en desservant en particulier la gare de Pont-Cardinet.</p>
<p>À partir de cette gare une trémie permettrait au trains de descendre vers une nouvelle gare, <a href="..\gare\ac2bga-europe-saint-lazare.php">Europe-Saint-Lazare</a>, souterraine et légèrement au nord de la gare Saint-Lazare, sous le carrefour de l'Europe. Cette dernière permettra de soulager le trafic de la gare Saint-Lazare tout en s'affranchissant du problème des quais courts que connait la gare principale.</p>
<p>Le tracé maintenant souterain passerait à l'ouest de la ligne 14 et rejoindrait la place de la Concorde avant de traverser la Seine à l'est du pont éponyme avec une station sous la Seine <a href="..\gare\d8ikjm-orsay-concorde.php">Orsay - Concorde</a> en correspondance avec Musée d'Orsay et Concorde. Ensuite le tracé irait vers le sud pour rejoindre le <a href="..\pole\13978n7-montparnasse.php">pôle de Montparnasse-Bienvenüe</a> via la gare de <a href="..\gare\m1flk4-laennec.php">Laënnec</a> dimensionnée pour servir de gare terminus pour les cas de perturbations ou d'évènements exceptionnels, en particulier le feux d'artifice du champ de Mars.</p>
<p>La sortie de Paris se ferait par la Porte d'Orléans puis en passant par Arcueil - Cachan, il traverserait la vallée de la Bièvre pour atteindre la gare de bifurcation de <a href="..\gare\hh3g39-lhay-les-roses-aqueduc.php">L'Haÿ-les-Roses - Aqueduc</a> en correspondance avec la ligne 4 et la ligne 14. Après avoir laissé la <a href="ag0gi4-rerf-verneuil.php">branche Verneuil-l'Etang</a> partir vers l'est, le tronc commun passerait par la gare routière du MIN de Rungis et contournerait le centre commercial de Belle Epine par l'est. Pour atteindre la gare du <a href="..\gare\8g273j-pont-de-rungis.php">Pont de Rungis</a>. L'itinéraire partagé avec le <a href="11d9i70-rerc-remaniement-orly.php">RER C dévié</a> atteindrait ensuite Orly-TGV contournerait l'aérogare Orly-Ouest par le nord ou par le sud en fonction des sous-sols pour se diriger vers Morangis, point de séparation entre les branches <a href="165d13k-rerf-etampes.php">Étampes</a> et <a href="10nnij9-rerf-lisses.php">Lisses</a> du RER F.</p>
<p>Cet axe serait a 4 voies et suffisament redondant pour qu'un accident grave de voyageur ou une operation de police ait peu d'impact sur la fluidité du trafic.</p>
<p>Entre Orsay-Concorde et Orly TGV, l'infrastructure serait partagée avec le service <a href="..\ligne\rer_aex.php">Aéroports express</a> La Défense - Orly.</p>
