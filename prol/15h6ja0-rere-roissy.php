<p id="line-subtitle">Une extension de la <a href="..\ligne\rer_e.php">ligne E</a> du RER.</p>
<p>La branche Roissy du RER E fournira un moyen simple et pratique pour se rendre de Saint-Cloud / Boulogne à l'aéroport de Roissy et sera utilisée par les trains du service <a href="..\ligne\rer_aex.php">aéroports express</a> entre la Défense et Roissy. En dehors du raccordement de Pantin (situé au niveau des sauts de moutons de Pantin-Bobigny), l'infrastructure serait partagée avec le RER G de Bobigny à Roissy.</p>
