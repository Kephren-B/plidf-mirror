<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_5.php">ligne 5</a> du métro.</p>
<p>L'extension au nord de la ligne 5 vise à améliorer les connexions entre la prefecture de Bobigny et le nord-est de la Seine-Saint-Denis. Elle permettra également de desservir les quartiers industriels du Nord du Blanc-Mesnil et d'Aulnay-sous-Bois.</p>
<p>Enfin elle permettra de rabattre les voyageurs vers les RER B (<a href="..\gare\em8b77-drancy-gare.php">Drancy</a>, <a href="..\gare\6j88nm-villepinte-gare.php">Villepinte</a>), E et G (<a href="..\gare\9miae3-garonor.php">Garonor</a>, <a href="..\gare\5kh3le-drancy-le-blanc-mesnil-lycees.php">Drancy - Le Blanc Mesnil</a>).</p>
<p>Le terminus de Villepinte fournira un nouvel accès au Parc des expositions.</p>
