<h1 id="top"> <img class="line-logo line-logo-network line-logo-network-rer" height="25" src="..\medias\logos\logo_rer.svg" title="RER" width="25"/><a class="line-link" href="..\ligne\rer_b.php"><img class="line-logo line-logo-line line-logo-line-rer-b" height="25" src="..\medias\logos\rer_B.svg" title="RER B" width="25"/></a> RER B Tronc commun quadruplé </h1>
<p> <em>Bourg-la-Reine</em> - <em>Aulnay-sous-Bois</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\rer_b.php">ligne B</a> du RER.</p>
<p>Le tronc commun du RER B pose des problèmes entre <a href="..\gare\98gf86-gare-du-nord.php">Gare du Nord</a> et <a href="..\gare\10994i6-bourg-la-reine-gare.php">Bourg-la-Reine</a>. Entre Gare du Nord et <a href="..\gare\njel5e-chatelet-les-halles.php">Chatelet-les-Halles</a>, le partage d'infrastructures saturées entre les RER B et D nuit gravement à la régularité des deux lignes et fragilise leur exploitation. Au sud de Châtelet, les stations de la ligne B sont trop rapprochées ce qui nuit à la vitesse moyenne des trains tout en amplifiant les retards par un effet de blocage d'un train par le précedent encore en gare.</p>
<p>L'operation de quadruplement du tronc commun consiterait à creuser un nouveau tunnel entre <a href="..\gare\hknhbk-bagneux-gare.php">Bagneux</a> et <a href="..\gare\19bickd-denfert-rochereau.php">Denfert Rochereau</a> sous la nationale 20 via <a href="..\gare\9cjfc5-porte-dorleans.php">Porte d'Orléans</a> par lequel transiteraient les trains semi-directs en provenance de <a href="..\gare\cichea-saint-remy-les-chevreuse.php">Saint-Rémy-les-Chevreuse</a> et <a href="m304d0-rerb-saint-quentin.php">Saint-Quentin-en-Yvelines</a>.</p>
<p>Les travaux seraient réalisés en commun avec les troncs communs du <a href="82636i-rerf-tronc-commun.php">RER F</a> (Gare de Porte d'Orléans commune avec le RER B) et du <a href="g8efg2-rerg-tronc-commun.php">RER G</a> (Base de travaux à Denfert-Rochereau).</p>
<p>À plus longue échéance, le nouveau tunnel serait prolongé pour desservir Luxembourg et Châtelet, sans desservir Port-Royal ni Saint-Michel pour maintenir une interstation suffisament longue et une bonne vitesse moyenne. La gare de Châtelet-les-Halles serait étendue et réaménagée avec deux niveaux de voies pour offrir 2 voies à quai pour chaque RER (A, B et D) et pour chaque sens ainsi que des quais plus large pour le RER D. De Châtelet-les-Halles un nouveau tunnel partirait vers la gare du Nord qui serait elle aussi étendue et réaménagée pour la jonction TGV Nord - Lyon - Montparnasse.</p>
<p>En complement de ces travaux, une nouvelle gare à <a href="..\gare\nn3g7l-porte-de-la-chapelle.php">Porte de la Chapelle</a> RER B D permettrait la correspondance avec le métro 12, le t3b et la <a href="..\ligne\rer_pce.php">Petite Ceinture Express</a> pour une meilleure liaison entre le nord et le sud de Paris.</p>
<p>On envisagera également la mise à 4 voies de la gare <a href="..\gare\maj975-la-croix-de-berny.php">la Croix de Berny</a> (cf le projet du <a href="ch779a-grand-orlyval.php">Grand Orlyval</a>) et du tronçon de la Fontaine-Michalon aux les Baconnets (réalisé en même temps que l'interconnexion LGV Sud par Orly TGV) qui peuvent être realisés à peu de frais.</p>
