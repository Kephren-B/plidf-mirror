<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\rer_b.php">ligne B</a> du RER.</p>
<p>Le projet d'extension du RER B de Robinson à Élancourt est un projet d'amélioration de la desserte de la banlieue sud-ouest de Paris. Le tracé passerait par Châtenay-Malabry, Vélizy, Buc et Guyancourt. Il permettrait de relier Vélizy à Saint-Quentin-en-Yvelines de manière simple et rapide et offrirait un accès au RER à un secteur actuellement très mal desservi.</p>
<p>En complément, comme le tracé est plus ou moins parallèle à la voie rapide A86/N12 il permettra un bon report du trafic de la route vers les transports en commun.</p>
