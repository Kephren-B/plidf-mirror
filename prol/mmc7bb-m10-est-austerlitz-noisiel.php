<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_10.php">ligne 10</a> du métro.</p>
<p>Le prolongement de la ligne 10 à Noisy-le-Grand, parallèle à l'autoroute A4 vise à un report du trafic automobile depuis l'autoroute vers le métro.</p>
<p>Il permettra de relier la zone d'Ivry-sur-Seine - Charenton à l'est du Val-de-Marne et à Marne-la-Vallée. La station <a href="..\gare\k9g1j0-parc-de-bercy.php">Parc de Bercy</a> permettra une nouvelle desserte du <abbr title="Palais omnisport de Paris Bercy">POPB</abbr> et de la bibliothèque François Mitterand.</p>
<p>En plus de cela, avec le métro 10 il sera possible d'aller simplement de Cours-Saint-Émilion à Charenton, ce qui est actuellement difficile à cause du Boulevard périphérique et de l'échangeur de Bercy (A4).</p>
