<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\rer_c.php">ligne C</a> du RER.</p>
<p>Avec la création des tangentielles Essonne, Val-de-Marne et Yvelines et le remaniement du transilien R, le RER C perdra une grande partie de son trafic sur la banlieue sud. Cette perte de trafic rendra possible l'intégration du service Paris-Corbeil-Essonnes par la vallée au RER C pour une meilleure desserte de l'agglomération d'Évry.</p>
