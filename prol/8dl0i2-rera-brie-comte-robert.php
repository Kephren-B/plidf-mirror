<p> <em>Boissy-Saint-Leger</em> - <em>Brie-Comte Robert</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\rer_a.php">ligne A</a> du RER.</p>
<p>Le prolongement du RER A à Brie-Comte-Robert correspond a une partie du projet de reouverture complète de la <a href="https://fr.wikipedia.org/wiki/Ligne_de_Vincennes" target="_blank">ligne de Vincennes à Marles-en-Brie</a> avec le <a href="ag0gi4-rerf-verneuil.php">RER F</a> et la <a href="1793dhj-tangentielle-seine-et-marne.php">tangentielle Seine-et-Marne</a>.</p>
<p>Les trains qui assureront le service jusqu'à Brie-Comte-Robert auront pour origine/terminus <a href="..\gare\c0k6mi-joinville-le-pont-gare.php">Joinville-le-Pont</a> où les voyageurs pourront choisir entre continuer sur le RER A et emprunter le <a href="mmc7bb-m10-est-austerlitz-noisiel.php">métro 10</a> ou le <a href="i4d43n-metro-sud-gretz-elancourt.php">métro Sud</a> pour se rendre à Paris.</p>
