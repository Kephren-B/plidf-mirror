<p> <em>Étoile</em> - <em>Villeneuve-la-Garenne</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_6.php">ligne 6</a> du métro.</p>
<p>Le prolongement nord-ouest de la ligne 6 est un projet proposé par le Plan Ile-de-France. Il consisterait à prolonger la ligne 6 de la station Étoile jusqu'à Villeneuve-la-Garenne. Cette extension permettrait de mieux relier le 17e arrondissement de Paris au RER A à la gare d'Étoile ce qui soulagerait les lignes <a href="..\ligne\m_2.php">2</a> et <a href="..\ligne\m_3.php">3</a> du métro.</p>
<p>La liaison vers <a href="..\gare\83bnfj-mairie-de-clichy.php">Mairie de Clichy</a> contribuera à soulager la ligne 13 branche Asnières (qui devrait être intégrée au <a href="..\ligne\m_nord.php">métro Nord</a>).</p>
<p>Il y aurait une correspondance avec le <a href="..\ligne\m_15.php">métro 15</a> et le RER C aux Grésillons.</p>
<p>Enfin ce prolongement permettrait de doter la ligne 6 d'un vrai terminus, par opposition à une boucle comme c'est le cas actuellement à Étoile et d'ateliers d'entretien à Villeneuve-la-Garenne mutualisés avec la ligne 4 qui permettront d'améliorer la robustesse de l'exploitation et augmenter la fréquence des trains pour mieux absorber les pointes de trafic.</p>
<p></p>
