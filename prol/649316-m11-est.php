<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_11.php">ligne 11</a> du métro.</p>
<p>Ce prolongement est la description du projet d'extension de la ligne 11 déjà en cours d'étude par le STIF. dans la partie Rosny-Bois-Perrier - Noisy-Champs nous proposons une modification qui consiste à décaler l'itinéraire vers le nord et l'est près Villemomble pour tenir compte de la <a href="h00ihb-rerg-claye-souilly.php">création du RER G</a>, du <a href="jbab92-m1-est-vincennes-chelles.php">prolongement du métro 1</a> et permettre une mutualisation des travaux de génie civil entre Gagny et Noisy Champs avec la <a href="c4fmej-metro-16-grand-paris-express.php">ligne 16</a>.</p>
<p>Notre version inclus également la création d'une station à <a href="..\gare\jdi4hh-pyramide-de-champs.php">Pyramide de Champs-sur-Marne</a>.</p>
<p><a href="http://www.prolongementligne11est.fr/">Site officiel du prolongement de la ligne 11</a> et sa section sur la deuxième partie <a href="http://noisychamps.prolongementligne11est.fr/">Rosny-sous-Bois - Noisy-Champs</a>.</p>
