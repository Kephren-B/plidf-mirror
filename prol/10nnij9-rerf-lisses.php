<h1 id="top"> <img height="25" src="..\medias\logos\logo_rer.svg" title="RER" width="25"/><a href="..\ligne\rer_f.php"><img height="25" src="..\medias\logos\rer_F.svg" title="RER F" width="25"/></a> RER F Lisses </h1>
<p> <em>Orly - TGV</em> - <em>Lisses</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_f.php">ligne F</a> du RER.</p>
<p>La branche Lisses du RER F serait une ligne nouvelle pour desservir Sainte-Geneviève-des-Bois et l'ouest du secteur Évry - Corbeil-Essonnes qui bénéficieraient d'un accès rapide vers l'aéroport d'Orly et l'ouest de Paris.</p>
<p>Cette desserte serait complémentaire à la ligne du plateau d'Evry (<a href="..\ligne\rer_d.php">RER D</a>) et lui serait reliée par le raccordement de Courcouronnes (<a href="..\gare\d90hk2-bondoufle-za.php">Bondoufle ZA</a> - <a href="..\gare\je2h7c-evry-courcouronnes.php">Évry - Courcouronnes</a>) utilisé par les trains de la <a href="..\ligne\tg_ess.php">tangentielle Essonnes</a> reliant Saint-Rémy-les-Chevreuse à Melun.</p>
<p>Une correspondance RER-autoroute (A6 + francilienne) pourra être mise en place à Lisses ou Courcouronnes.</p>
