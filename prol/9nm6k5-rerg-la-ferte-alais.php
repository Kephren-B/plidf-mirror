<p id="description"> </p>
<p id="line-subtitle">Création de la <a href="..\ligne\rer_g.php">ligne G</a> du RER.</p>
<p>Descendant vers le sud de l'Île-de-France cette branche du RER G permettra de relier la <a href="..\pole\dgddel-saclay-orsay.php">zone universitaire de Saclay</a> avec le sud et l'est de l'Essonnes. Elle permettra également d'assurer la correspondance avec les RER C, D et F ainsi qu'avec le transilien R. L'infrastructure entre Bourray et Ballancourt-sur-Essonnes sera partagée avec le <a href="ggj1f5-trr-malesherbes.php">transilien R Malesherbes</a>.</p>
