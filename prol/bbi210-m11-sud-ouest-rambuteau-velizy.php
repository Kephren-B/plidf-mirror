<h1 id="top"> <img class="line-logo line-logo-network line-logo-network-m" height="25" src="..\medias\logos\logo_m.svg" title="Métro" width="25"/><a href="..\ligne\m_11.php"><img class="line-logo line-logo-line line-logo-line-m-11" height="25" src="..\medias\logos\m_11.svg" title="Métro 11" width="25"/></a> M11 Sud-Ouest - Vélizy </h1>
<p> <em>Châtelet - Rambuteau</em> - <em>Vélizy - Base aérienne</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_11.php">ligne 11</a> du métro.</p>
<p>La ligne 11 qui se termine au milieu de Paris à Châtelet-les-Halles n'est pas exploitée au maximum de son potentiel.</p>
<p>Le prolongement à Vélizy servirait à créer une nouvelle connexion rapide entre Châtelet et <a href="..\gare\ih496i-montparnasse-bienvenue.php">Montparnasse - Bienvenue</a>, améliorer la desserte du 14e arrondissement de Paris et offrir un nouveau moyen rapide pour se rendre à Paris depuis les villes de Vanves, Clamart, Meudon et Vélizy. La desserte du parc des expositions de la porte de Versailles par le sud pourrait également intéresser les usagers se rendant à un salon.</p>
<p>Le métro 11 serait detourné à partir de Rambuteaux pour partir vers l'ouest , desservir le pôle Châtelet depuis une station située sous la rue Berger, moins excentrée par rapport à l'actuelle station situé près de la Seine. Après cela le nouveau tracé partirait en direction du sud vers Montparnasse-Bienvenüe via <a href="..\gare\111bgh0-saint-germain-des-pres.php">Saint-Germain-des-Prés</a>. On continurait ensuite vers la <a href="..\gare\7a7f2i-porte-brancion-ouest-ceinture.php">Porte Brancion</a>, pour traverser les villes de Vanves et de Clamart avec une station de métro pour les mairies de chaque commune traversée.</p>
<p>Après avoir desservi l'<a href="..\gare\707d74-hopital-antoine-beclere.php">hôpital Antoine Beclère</a> le tracé partirait vers l'ouest et rejoindrait la gare de <a href="..\gare\13ic4i6-meudon-velizy.php">Meudon - Vélizy</a> puis la base aérienne de Villacoublay son terminus.</p>
<p>Un service (ligne 11 bis) comparable à l'ancienne voie navette relierait Rambuteaux à Châtelet Pont Marie par le tunnel abandonné pour le prolongement de la ligne principale.</p>
