<p id="line-subtitle">Une extension de la <a href="..\ligne\m_7.php">ligne 7</a> du métro.</p>
<p>Le long de la nationale 2 et puis de la nationale 17, ce prolongement du métro est attendu par tout ceux qui ont fréquenté le musée de l'air et de l'espace ou les divers évènements qui se sont tenus au Bourget.</p>
<p>Le voyageurs désirant se rendre à l'aéroport de Roissy pourront continuer leur trajet par la <a href="..\ligne\m_17.php">ligne 17 du métro</a>.</p>
