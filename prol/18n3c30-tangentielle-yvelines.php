<p id="description"> </p>
<p id="line-subtitle">Une <a href="..\ligne\tg_y.php">nouvelle tangentielle</a> pour le réseau Île-de-France</p>
<p>La tangentielle Yvelines serait un nouvel axe de transport qui relierait les pôles de <a href="..\pole\14c153l-cergy-pontoise.php">Cergy-Pontoise</a> et de <a href="..\pole\a75iii-massy-palaiseau.php">Massy-Palaiseau</a>. Elle reprendrait l'infrastructure de la ligne de la Grande Ceinture sur la plus grande partie de son tracé. Elle permettrait à de nombreux voyageurs de transiter dans l'ouest de l'Île-de-France sans avoir à passer par Paris.</p>
<p>De nombreuses correspondances permettraient aux voyageurs qui l'emprunteraient de se rendre à l'aéroport de Roissy, la Défense, Mantes-la-Jolie ou Saint-Quentin-en-Yvelines. À ses deux extremités elles serait en correspondance d'autres tangentielles: <a href="..\ligne\tg_vo.php">tangentielle Val-d'Oise</a> à <a href="..\gare\eggnif-conflans-fin-doise.php">Conflans-Fin-d'Oise</a> et tangentielles <a href="..\ligne\tg_ess.php">Essonnes</a> et <a href="..\ligne\tg_vdm.php">Val-de-Marne</a> à <a href="..\gare\k7a4hf-massy-palaiseau.php">Massy - Palaiseau</a>.</p>
