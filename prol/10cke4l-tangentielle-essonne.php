<p id="description"> </p>
<p id="line-subtitle">Une <a href="..\ligne\tg_ess.php">nouvelle tangentielle</a> pour le réseau Île-de-France</p>
<p>La tangentielle Essonne serait un service destiné à relier les pôles importants de l'Essonne en particulier la vallée de l'Yvette et Évry. Elle partagerait les voies du RER B entre <a href="..\gare\cichea-saint-remy-les-chevreuse.php">Saint-Rémy-les-Chevreuse</a> et <a href="..\gare\k7a4hf-massy-palaiseau.php">Massy - Palaiseau</a>, puis utiliserait la ligne de Grande Ceinture entre Massy-Palaiseau et Petit-Vaux. Elle rejoindrait Melun après un itinéraire commun avec le RER F, branche Lisses jusqu'à Évry puis remonterait la vallée de la Seine par la ligne de Corbeil-Essonnes à Montereau.</p>
<p>Cette ligne permettrait de rationnaliser les dessertes Massy-Palaiseau - Petit-Vaux et Melun - Corbeil-Essonnes en les transformant explicitement en dessertes de type rocade et plus radiales comme c'est le cas actuellement par leur intégration respectivement dans les RER C et D.</p>
