<h1 id="top"> <img class="line-logo line-logo-network line-logo-network-rer" height="25" src="..\medias\logos\logo_rer.svg" title="RER" width="25"/><a class="line-link" href="..\ligne\rer_e.php"><img class="line-logo line-logo-line line-logo-line-rer-e" height="25" src="..\medias\logos\rer_E.svg" title="RER E" width="25"/></a> RER E Meaux </h1>
<p> <em>Chelles-Gournay</em> - <em>Meaux</em> </p>
<div>
<a class="section-link top-link" href="#maps-section">↓ Aller au tracé</a>
</div>
<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\rer_e.php">ligne E</a> du RER.</p>
<p>Le RER E serait prolongé à Meaux grâce à l'augmentation du trafic possible sur le tronc commun suite aux prolongements à l'ouest. Il intégrerait une desserte actuellement incluse dans le transilien P.</p>
<p>Ce prolongement reprenant les infrastuctures existantes s'accompagnerait de la mise à quatre voies de la ligne entre Lagny - Thorigny et Meaux ainsi que de la création de trois nouvelles gares à <a href="..\gare\19566k3-dampmart.php">Dampmart</a>, <a href="..\gare\i5l9m4-mareuil-les-meaux.php">Mareuil-les-Meaux</a> et <a href="..\gare\10ab19d-meaux-est.php">Meaux</a>. La gare de <a href="..\gare\hine75-trilport.php">Trilport</a> serait réaménagée pour être une gare terminus pour les RER E et G, le transilien P (<a href="1145m57-transilien-meaux-coulommiers.php">liaison Meaux-Coulommiers</a>) et pour la tangentielle Val-d'Oise.</p>
