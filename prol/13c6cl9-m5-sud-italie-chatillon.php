<p id="description"> </p>
<p id="line-subtitle">Une extension de la <a href="..\ligne\m_5.php">ligne 5</a> du métro.</p>
<p>Le prolongement de la ligne 5 vers Châtillon-Montrouge est un projet proposé par le Plan Ile-de-France. Cette connexion de la place d'Italie à Châtillon permettra de rendre plus accessible des secteurs du XIIIe arrondissement de Paris. Ce projet permettra aux voyageurs arrivant de la banlieue sud par le RER B, le RER F, le métro 4 ou le T6 d'accéder facilement à la gare d'Austerlitz et au secteur de Bastille - Canal-Saint-Martin.</p>
<p>Avec ce prolongement la ligne 5 sera en correspondance avec la ligne 15 à son terminus ce qui pourra interesser des voyageurs qui souhaitent se rendre à la Défense.</p>
