<div id="description">
<p>La station de métro de la Porte de la Chapelle est une station de métro importante de la ligne 12 an tant qu'ancien terminus. Le plan Ile-de-France propose d'en faire un grand pôle de correspondances du nord de Paris par la réouverture de l'ancienne gare de la Chapelle-Saint-Denis dans le cadre du projet petite ceinture express. Cette réouverture serait complétée par la création d'une gare RER B/D Porte de la Chapelle.</p>
