<h1>Le Cergyval</h1>
<div class="main-img-div">
<img class="main-img" src="..\medias\img\cdgval_croisement.jpg" width="480"/>
</div>
<section>
<p><em id="resume">Un réseau de VAL pour desservir la ville nouvelle de Cergy - Pontoise.</em><br/><small><a href="#trace">Voir le tracé</a></small></p>
<p>La ville nouvelle de <a href="..\pole\14c153l-cergy-pontoise.php">Cergy-Pontoise</a> est le résultat d'un projet d'urbanisme datant des années 1960 dont le but était de canaliser l'urbanisation de l'Île-de-France.<br/> Ce projet a été un succès et aujourd'hui le secteur <a href="..\commune\Cergy.php">Cergy</a> – <a href="..\commune\Pontoise.php">Pontoise</a> – <a href="..\commune\Saint-Ouen-l'Aumone.php">Saint-Ouen-l'Aumône</a> est l'un des secteurs les plus attractifs de l'Île-de-France hors de Paris, pour se loger, pour étudier ou pour travailler.</p>
<p>L'urbanisation de Cergy s'est faite le long d'un axe routier, le <em>Boulevard de l'Oise</em>, qui s'étend de <a href="..\gare\13ilg55-cergy-prefecture.php">Cergy-Préfecture</a> à <a href="..\commune\Jouy-le-Moutier.php">Jouy-le-Moutier</a>.<br/> Parallèlement à cet axe une nouvelle ligne de chemin de fer, <a class="wikilink" href="https://fr.wikipedia.org/wiki/Ligne_de_la_bifurcation_de_Neuville_%C3%A0_Cergy-Pr%C3%A9fecture">la ligne de Cergy</a>, a été construite pour relier les secteurs nouvellement urbanisés à Paris. Elle a été intégrée plus tard au RER A et prolongée à deux reprises de Cergy-Préfecture à <a href="..\gare\l58mk2-cergy-saint-christophe.php">Cergy-Saint-Christophe</a> puis <a href="..\gare\103mchc-cergy-le-haut.php">Cergy-le-Haut</a>.</p>
<p>Aujourd'hui la ligne de Cergy dessert deux des trois grands pôles de transports de Cergy-Pontoise: La gare de Cergy-le-Haut et la gare de Cergy-Préfecture. Elle n'est pas reliée de manière efficace au troisième grand pôle de la ville nouvelle: la <a href="..\gare\11h6ha9-pontoise-gare.php">gare de Pontoise</a>.</p>
<p>L'absence de connexion entre la ligne de Cergy et les autres lignes de chemin de fer qui passent dans le voisinage de la ville nouvelle font qu'il n'est pas simple de se déplacer de Cergy pour aller à Saint-Ouen-l'Aumône, à <a href="..\commune\Poissy.php">Poissy</a>, aux <a href="..\commune\Les_Mureaux.php">Mureaux</a> ou à <a href="..\commune\Mantes-la-Jolie.php">Mantes-la-Jolie</a>.</p>
<p>A ce problème de déplacement s'ajoute un problème de canalisation de l'evolution de la ville nouvelle: l'urbanisation de la commune de Cergy approche la saturation et maintenant le dévelopement de Cergy-Pontoise se fait le long de la vallée de l'Oise, quasiment jusqu'à <a href="..\commune\Conflans-Sainte-Honorine.php">Conflans-Sainte-Honorine</a> et loin des infrastructures ferroviaires existantes.</p>
<p>Le réseau de Cergyval avec ses deux lignes entend corriger ces problèmes de mobilité.</p>
<p>La <a href="..\prol\1948n11-cergyval1-epluches-chambourcy.php">ligne 1 du Cergyval</a> irait de Saint-Ouen-l'Aumône à Poissy en passant par le nord de <a href="..\commune\Pontoise.php">Pontoise</a>, <a href="..\commune\Osny.php">Osny</a>, Cergy-Saint-Christophe et <a href="..\gare\eggnif-conflans-fin-doise.php">Conflans-Fin-d'Oise</a>.</p>
<p>La <a href="..\prol\162fk0m-cergyval2-epluches-les-mureaux.php">ligne 2 du Cergyval</a> irait de Saint-Ouen-l'Aumône aux Mureaux en passant par la gare de Pontoise et Cergy-Préfecture. Les deux lignes auraient une section commune de quelques kilomètres entre <a href="..\commune\Vaureal.php">Vauréal</a> et le <a href="..\gare\f1k67d-pont-de-neuville.php">pont de Neuville</a>.</p>
