<h1>Les tangentielles</h1>
<div class="main-img-div">
<img class="main-img" src="..\medias\img\massy_pal_passerelle.jpg" width="480"/>
</div>
<section>
<p>Nos propositions incluent la création d'un réseau de tangentielles pour canaliser les déplacements de banlieues à banlieue.</p>
<p>Nous avons six propropositions qui correspondent à 6 des 8 départements de l'Île-de-France.</p>
<dl>
<dt>
<span class="inline-logo"><img height="25" src="..\medias\logos\logo_tg.svg" title="Tangentielle" width="25"/><a href="..\ligne\tg_ssd.php"><img height="25" src="..\medias\logos\tg_ssd.svg" title="Tangentielle Seine-Saint-Denis" width="25"/></a></span>
<a href="..\ligne\tg_ssd.php">La tangentielle Seine-Saint-Denis</a>
</dt>
<dd>
           Inscrite dans la planification du STIF sous le nom de 
          <a href="http://www.tangentiellenord.fr/" target="_blank" title="site officiel">tangentielle Nord</a>, son tronc central est actuellement (janvier 2016) en cours de construction entre 
          <a href="..\gare\nd30n6-epinay-sur-seine.php">Épinay-sur-Seine</a> et 
          <a href="..\gare\141finl-le-bourget.php">le Bourget</a>. 
         </dd>
<dd>
           Par rapport au STIF nous proposons de l'étendre à 
          <a href="..\gare\1679e0i-poissy-gare.php">Poissy</a> à l'ouest et à 
          <a href="..\gare\7an56f-chelles-gournay.php">Chelles - Gournay</a> à l'est. 
         </dd>
<dt>
<span class="inline-logo"><img height="25" src="..\medias\logos\logo_tg.svg" title="Tangentielle" width="25"/><a href="..\ligne\tg_vdm.php"><img height="25" src="..\medias\logos\tg_vdm.svg" title="Tangentielle Val-de-Marne" width="25"/></a></span>
<a href="..\ligne\tg_vdm.php">La tangentielle Val-de-Marne</a>
</dt>
<dd>
           Reprenant les infrastructures de la grande ceinture, cette tangentielle simplifierait les deplacements à travers le Val-de-Marne entre Chelles-Gournay et 
          <a href="..\gare\k7a4hf-massy-palaiseau.php">Massy - Palaiseau</a>. 
         </dd>
<dt>
<span class="inline-logo"><img height="25" src="..\medias\logos\logo_tg.svg" title="Tangentielle" width="25"/><a href="..\ligne\tg_ess.php"><img height="25" src="..\medias\logos\tg_ess.svg" title="Tangentielle Essonne" width="25"/></a></span>
<a href="..\ligne\tg_ess.php">La tangentielle Essonne</a>
</dt>
<dd>
           Pour le moment envisagée comme un petit tram-train entre Massy et Évry par le STIF, nous proposons d'en faire une vraie liaison du sud de l'Ile-de-France entre Melun et Saint-Rémy-les-Chevreuse. 
         </dd>
<dt>
<span class="inline-logo"><img height="25" src="..\medias\logos\logo_tg.svg" title="Tangentielle" width="25"/><a href="..\ligne\tg_y.php"><img height="25" src="..\medias\logos\tg_y.svg" title="Tangentielle Yvelines" width="25"/></a></span>
<a href="..\ligne\tg_y.php">La tangentielle Yvelines</a>
</dt>
<dd>
           Nommée 
          <a href="http://www.tangentielleouest.fr/" target="_blank" title="Site officiel">tangentielle ouest</a> dans les plans du STIF, elle à déjà été commencée avec la de réouverture de la Grande-Ceinture-Ouest. Le STIF prévoit de la faire aller d'Achères a Saint-Cyr. 
         </dd>
<dd>
          Nous proposons de l'étendre à 
          <a href="..\gare\103mchc-cergy-le-haut.php">Cergy-le-Haut</a> au nord et à Massy-Palaiseau au sud 
         </dd>
<dt>
<span class="inline-logo"><img height="25" src="..\medias\logos\logo_tg.svg" title="Tangentielle" width="25"/><a href="..\ligne\tg_vo.php"><img height="25" src="..\medias\logos\tg_vo.svg" title="Tangentielle Val-d'Oise" width="25"/></a></span>
<a href="..\ligne\tg_vo.php">La tangentielle Val d'Oise</a>
</dt>
<dd>
           Empruntant de nouvelles infrastructures dans le nord du Val-d'Oise entre 
          <a href="..\gare\bg4eg3-valmondois.php">Valmondois</a> et 
          <a href="..\gare\18b0hdf-claye-souilly-est.php">Claye-Souilly</a>, elle permettra de connecter l'ouest du Val-d'Oise et la Normandie à l'
          <a href="..\gare\15jd1k8-cdg3.php">aéroport de Roissy</a>. 
         </dd>
<dt>
<span class="inline-logo"><img height="25" src="..\medias\logos\logo_tg.svg" title="Tangentielle" width="25"/><a href="..\ligne\tg_sm.php"><img height="25" src="..\medias\logos\tg_sm.svg" title="Tangentielle Seine-et-Marne" width="25"/></a></span>
<a href="..\ligne\tg_sm.php">La tangentielle Seine-et-Marne</a>
</dt>
<dd>
           Utilisant des infrastructures nouvelles ou recréées et partagées avec la tangentielle Val-d'Oise et le RER G, elle permettra de relier l'aéroport de Roissy au le pôle de 
          <a href="..\gare\13cj413-mlv-chessy.php">Marne-la-Vallee - Chessy</a> et à la ville nouvelle de Melun-Sénart. La connexion Chessy - Roissy répondant à une grande attente des voyageurs. 
         </dd>
<dd>
           Avec cette tangentielle il n'y aura plus besoin de passer par Paris pour se déplacer en train dans la Seine-et-Marne. 
         </dd>
</dl>
