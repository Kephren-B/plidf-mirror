<h1>Le Plan Ile-de-France - Qu'est ce que c'est?</h1>
<div class="main-img-div">
<img class="main-img" src="..\medias\img\meriel_soleil.jpg" width="480"/>
</div>
<section>
<p>Une équipe de citoyens constatant les graves difficultés de circulation en Île-de-France s'est rassemblée pour réflechir sur la manière d'étendre le réseau de transports ferrés de notre region pour régler les difficultés liées à la saturation des infrastructures existantes.<br/> Nous voulons également rassembler un annuaire d'associations locales d'usagers des tranports en commun pour aider les franciliens qui ont trop de problèmes dans les transports à se rassembler pour trouver des solutions.</p>
<p>Notre site contient à la fois une banque d'<a href="..\index\prolongements.php">idées d'extensions du réseau de transports ferrés</a> en Île-de-France et un <a href="..\index\associations.php">annuaire d'associations d'usagers</a> des transports en commun trié par commune.</p>
<p>Vous pourrez probablement trouver la commune où vous habitez dans notre <a href="..\index\communes.php">liste des communes</a> ou <a href="..\index\gares.php">une gare qui vous intéresse</a>.</p>
<p>Si vous êtes intéressés par nos idées où que vous avez vous-même d'autres idées en lien avec les transports en commun, nous vous invitons à participer à notre initiative de plusieurs manières. </p>
<ul class="normal-ul">
<li>Vous pouvez relayer l'existence de notre site sur votre blog et en disant ce que vous pensez de nos propositions. En échange nous publierons des liens vers tous les blogs qui ont relayé notre projet.</li>
<li>Vous pouvez nous envoyer des propositions architecturales pour l'une des gares à créer parmi toutes celles qui sont proposées dans notre projet, que nous publierons.</li>
<li>Vous pouvez créer une association locale d'usagers des transports en commun ou rejoindre une association existante. </li>
<li>Vous pouvez nous transmettre vos participations à des débats publics sur des extensions de transports en commun que nous relayerons sur notre plateforme.</li>
</ul>
<p></p>
</section>
