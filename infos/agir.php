<h1>Participer à l'initiative Plan Île-de-France</h1>
<div class="main-img-div">
<img class="main-img" src="..\medias\img\feu_vert.jpg" width="480"/>
</div>
<section>
<p>Le plan Île-de-France (PLIDF) est une initiative citoyenne pour l'amélioration des transports en commun et appelle toutes les bonnes volontés à participer sur le sujet. Notre projet s'artcule entre planification de l'avenir des transports et résolution immédiate des problèmes quotidiens.</p>
<h3>Penser l'avenir des transports en commun</h3>
<p>Beaucoup d'initiatives de transports en commun ne voient jamais le jour parce que ceux qui ont les idées ne pensent pas à se rassembler. Et les idées de nouvelles lignes de métro ne sont connues que tardivement par les intéressés.</p>
<p>Avec le PLIDF vous aurez réfléchi à de nouvelles idées de transports en commun et vous ne serez pas pris de court par les prochains débats publics qui seront organisés par les autorités en charge des transports.</p>
<h3>Agir sur le court et le moyen terme</h3>
<p>Les plans que nous proposons sont des planifications pour le long terme mais il est important de tenir compte du court terme. Il y a beaucoup de grèves et de problèmes de pannes dans les transports, chaque jour qui passe apport son lots d'incident qui fatiguent ou agacent les franciliens qui empruntent les transports en commun. Ces problèmes ne sont pas une fatalité, ils peuvent être résolus. Ils faut que les franciliens reprennent la main sur la planification et la gestion des transports en commun. Pour cela il y a une voie à suivre:</p>
<h4>Se regrouper en association pour se faire entendre</h4>
<p id="creer">La <strong>première étape</strong> de l'action est de <strong><a href="..\index\associations.php">créer ou rejoindre</a> une association locale d'usagers des transports en commun</strong> par exemple les usagers du <a href="..\ligne\rer_a.php">RER A</a> habitués à prendre le RER à <a href="..\commune\Noisy-le-Grand.php">Noisy-le-Grand</a> pourront se regrouper en une association noiséenne des usagers du RER A. Ensuite organisez des évènements pour permettre aux différents usagers de se rencontrer, utilisez ces rencontres pour partager des informations et vous organiser pour limiter les problèmes posés par les perturbations ou les mouvements sociaux.</p>
<p style="text-align:center"><a href="..\index\commune.php">Trouver une association dans votre commune</a></p>
<h4>Passer à l'action</h4>
<p>Organisez des rencontres avec vos élus pour les convaincre de soutenir le(s) projet(s) qui vous interessent.</p>
<h3 style="display:none">S'exprimer</h3>
<h3>Participer à des débats publics</h3>
<p>Organisez une veille sur les différents projets qui existent en Île-de-France. Veillez à ce que des membres de votre association soient présents aux débats publics sur les extension de transports. Si certains projets vous semblent mauvais ou incohérents préparez un dossier et demandez une audience auprès du <abbr title="Syndicat de Transports en Île-de-France">STIF</abbr>.</p>
