<h1>Grand Paris Express</h1>
<div class="main-img-div">
<img class="main-img" src="..\medias\img\massy_pal_passerelle.jpg" width="480"/>
</div>
<section>
<em style="display:block;margin-bottom:2ex;">Une solution pour les déplacement en petite et moyenne banlieue.</em>
<p>Le Grand Paris Express est un projet de réalisation de lignes de métro en banlieue parisienne pour simplifier les déplacements de banlieue à banlieue. Ce problème et la saturation de l'autoroute A86 sont en discussion depuis des années. Plusieurs idées ont été avancées pour le résoudre et ont été compilées dans le projet du Grand Paris Express. Ce dernier inclus le prolongement de deux lignes de métro existantes (les lignes <a href="..\ligne\m_11.php">11</a> et <a href="..\ligne\m_14.php">14</a>) et la création de 4 nouvelles lignes de métro de rocade.</p>
<p>Nous avons inclus dans nos plans des idées pour chacune des lignes incluses dans le projet Grand Paris Express avec quelques mofidications pour tenir compte des idées de prolongements des autres lignes de métro et de la création des RER F et G:</p>
