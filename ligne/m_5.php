<h1 id="top">Métro 5</h1>
<p><em>(Villepinte) Bobigny-Pablo Picasso - Place d'Italie (Châtillon - Montrouge) </em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>La ligne 5 du métro est une ligne de métro qui traverse Paris suivant un axe nord-sud. Bien que la ligne desserve la gare du Nord, République et la gare d'Austerlitz, elle reste relativement peu fréquentée comparée à la ligne 4.</p>
<p>Le Plan Ile de France propose de prolonger la ligne au Nord jusqu'au parc des expositions de Villepinte en passant par Drancy, le Blanc-Ménil et Aulnay-sous-Bois.</p>
<p>Au sud, le STIF envisage l'extension de la ligne jusqu'à la place de Rungis dans le XIIIe arrondissement de Paris, le Plan Ile-de-France propose de l'étendre jusqu'à <a href="..\gare\18kga98-chatillon-montrouge.php">Châtillon - Montrouge</a> pour améliorer le maillage du réseau de métro et de RER au sud de Paris.</p>
</div>
