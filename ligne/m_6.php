<h1 id="top">Métro 6</h1>
<p><em>Nation - Étoile (Villeneuve-la-Garenne) </em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>La ligne 6 du métro est la portion sud de la rocade intérieure de Paris. Le Plan Ile-de-France propose de la polonger à l'ouest depuis Étoile vers Villeneuve-la-Garenne pour soulager les lignes de métro 2, 3 et 13. Cette extension permettrait de doter la ligne d'un terminus plus capacitaire que la boucle actuelle à Étoile nécéssaire à la fluidification du trafic en particulier en heures de pointes. Les métro n'auront ainsi plus à marquer de pause de régulation à Kléber comme c'est le cas actuellement.</p>
