<h1 id="top">Métro 3</h1>
<p><em>(Le Plessis-Bouchard) Pont de Levallois -  Galliéni (Mairie du Plessis-Trévise)</em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>La ligne 3 du métro est une ligne de métro orientée suivant un axe est-ouest. Elle est régulièrement saturée entre République et Porte de Champerret mais relativement peu utilisée hors de ce tronçon.</p>
<p>Le plan Ile-de-France propose de l'étendre à <a href="..\gare\13an1ff-val-de-fontenay.php">Val de Fontenay</a> et Villiers-sur-Marne vers l'est et à Franconville via la gare de Bécon-les-Bruyères, Colombes et Argenteuil à l'ouest.</p>
<p>La correspondance à la <a href="..\gare\gibnnb-becon-les-bruyeres-gare.php">gare de Bécon-les-Bruyères</a>, la création du <a href="m_nord.php">métro Nord</a>, et <a href="..\prol\103an0n-m6-ouest-etoile-villeneuve.php">l'extension de la ligne 6 à Villeneuve-la-Garenne</a> proposée par le Plan-Ile-de-France vont rémedier partiellement à son problème de saturation à l'ouest.</p>
</div>
