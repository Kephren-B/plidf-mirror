<h5>Limitation du RER C à Brétigny au profit du <a href="tr_r.php">transilien R</a></h5>
<p>En complément, le Plan Ile-de-France envisage la possibilité de limiter le RER C à Brétigny et d'intégrer les services qui continuent au delà de cette gare au transilien R. La gare de Brétigny serait alors profondément remaniée pour être le terminus du RER C, du RER G et de la tangentielle Seine-et-Marne intérieure.</p>
<h5>Abandon de la desserte de Saint-Quentin-en-Yvelines</h5>
<p>La desserte de Saint-Quentin-en-Yvelines serait laissée au RER G. En contre partie, un service Chaville-Vélizy - Champ de Mars serait mis en place pour assurer une desserte de 2 trains par quart d'heure entre Chaville et Champ de Mars.</p>
<p>Avec tout ces remaniements, le schéma de desserte du RER C serait le suivant: sur une trame de un quart d'heure: 1 aller retour Pontoise - Juvisy, 1 Montigny-Beauchamp (ou Ermont-Eaubonne) - Corbeil-Essonnes et 1 Versailles-Château - Brétigny et 1 Chaville-Vélizy - Champs de Mars.</p>
