<h1 id="top">RER C</h1>
<p><em>Pontoise / Versailles-Château/ Saint-Quentin-en-Yvelines - Étampes / Dourdan / Massy-Palaiseau</em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>Le RER C est une ligne de RER qui dessert la moitié ouest de l'Île-de-France. Il souffre dans sa partie parisienne d'un tracé sinueux et de vitesse limite très basses. Le Plan Ile-de-France propose de le remanier pour tenir compte de la création de nouvelles lignes de transport. La branche Massy-Palaiseau serait détournée par <a href="..\gare\15ba141-orly-tgv.php">Orly TGV</a>, la desserte <a href="..\gare\jib834-versailles-chantiers.php">Versailles - Chantiers</a> - <a href="..\gare\g02nh9-juvisy-sur-orge.php">Juvisy</a> intégrée aux <a href="tg_y.php">tangentielles Yvelines</a> et <a href="tg_ess.php">Essonne</a> et la desserte Brétigny - Dourdan serait intégrée au <a href="tr_r.php">transilien R</a>.</p>
<p>Les voyageurs de la ligne C bénéficieront largement de la création des <a href="rer_f.php">RER F</a> et <a href="rer_g.php">G</a>, celle du <a href="m_sud.php">métro Sud</a> et <a href="..\prol\m304d0-rerb-saint-quentin.php">l'extension du RER B à Saint-Quentin-en-Yvelines</a>.</p>
<h4>Remaniement complémentaire du RER C</h4>
<br/>
<h5>Suppression de la branche Massy, remplacée par la tangentielle Val-de-Marne.</h5>
<p>Avec tous les prolongements de métro et la création du RER F. La branche Massy du RER C ne sera plus le meilleur moyen de rejoindre Paris à partir des gare. Le Plan Ile-de-France envisage sa suppression au profit de la tangentielle Val-de-Marne et la création d'une nouvelle gare à Port d'Orly pour assurer la correspondance entre le RER C et la tangentielle. Cette suppression permettra d'augmenter la fréquence des trains entre Paris et Juvisy.</p>
