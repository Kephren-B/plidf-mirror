<h1 id="top">Métro 18</h1>
<p><em>Orly TGV - Saint-Quentin-en-Yvelines</em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>Une nouvelle ligne de métro pour relier le <a href="..\pole\dgddel-saclay-orsay.php">plateau de Saclay</a> à <a href="..\pole\hjn6fn-saint-quentin-en-yvelines.php">Saint-Quentin-en-Yvelines</a> et <a href="..\gare\15ba141-orly-tgv.php">l'aéroport d'orly</a>. Ce métro utilisant la technologie VAL partagerait les infrastructures de l'orlyval entre Orly TGV et <a href="..\gare\fcdcad-chemin-dantony.php">Chemin d'Antony</a> avant de partir vers le sud-ouest.</p>
<p>Par rapport au plan du Grand Paris, le terminus est fixé a Fontenay-le-Fleury au lieu de Rueil-Malmaison, la liaison vers les Hauts-de-Seine étant laissée au métro Sud, au métro 9, au transilien U, à Orlyval et au RER E.</p>
