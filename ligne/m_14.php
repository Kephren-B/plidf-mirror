<h1 id="top">Metro 14</h1>
<p><em>(Saint-Denis-Pleyel, Mairie de Saint-Ouen) Saint-Lazare - Olympiades (Orly-Sud, Vigneux-sur-Seine) </em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>La ligne 14, dernière née du réseau RATP, est une ligne qui suit une axe nord-ouest - sud-est dans Paris. Le projet Grand Paris Express Prévoit de l'étendre à Pleyel au Nord et à l'aéroport d'Orly au Sud.</p>
<p>Sur la partie Sud,le plan Île-de-France propose de décaler le tracé vers l'ouest pour tenir compte de la création de la ligne F du RER et d'étendre la ligne 14 jusqu'à Vigneux-sur-Seine via Athis-Mons.</p>
</div>
