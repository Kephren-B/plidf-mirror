<h1 id="top">Métro 4</h1>
<p><em>(Montmorency, Épinay-Villetaneuse) Porte de Clignancourt - Mairie de Montrouge (Massy) </em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>La ligne 4 du métro est une ligne de métro qui suit un axe nord-sud dans Paris. Elle est l'une des lignes les plus fréquentées du métro parisien et dessert plusieurs grand pôles de la ville de Paris.</p>
<p>Le Plan Ile-de-France propose de l'étendre au nord vers Montmorency via <a href="..\gare\d801ik-mairie-de-saint-ouen.php">Mairie de Saint-Ouen</a> et <a href="..\gare\199jlm6-epinay-villetaneuse.php">Épinay - Villetaneuse</a>.</p>
<p>Au sud l'extension de la ligne vers <a href="..\gare\95kk1d-bagneux-victor-hugo.php">Bagneux - Victor Hugo</a> est en construction, le Plan Ile-de-France propose de l'étendre encore plus au sud jusqu'à Massy en correspondance avec la <a href="m_14.php">ligne 14</a> et le <a href="rer_f.php">RER F</a> à la <a href="..\gare\hh3g39-lhay-les-roses-aqueduc.php">gare de l'aqueduc</a>.</p>
<p>La création du <a href="m_nord.php">métro Nord</a>, du <a href="m_sud.php">métro Sud</a>, du <a href="rer_g.php">RER G</a> et <a href="..\prol\649316-m11-est.php">l'extension de la ligne 11</a> devraient soulager la ligne d'un grande partie de son trafic sur sa section centrale.</p>
</div>
