<h1 id="top">Aéroports express</h1>
<p><em>La Défense, Montparnasse - Orly - Roissy</em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>Le service Aéroport Express est une proposition du Plan Île-de-France pour mieux relier les grand pôles Parisiens aux aéroports de Roissy et l'Orly. Les trains de cette ligne utiliseraient les infrastructures des RER A, E, F et G pour assurer une desserte rapide des aéroports de Roissy et d'Orly au départ de la Défense, Étoile, Montparnasse et gare de Lyon.</p>
</div>
