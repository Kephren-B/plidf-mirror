<h1 id="top">RER G</h1>
<p><em>Senlis / Trilport / Claye-Souilly / Coulommiers - Plaisir-Grignon / Élancourt / Saint-Arnoult-en-Yvelines / La Ferté-Alais</em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>Le RER G serait une nouvelle ligne de RER orientée nord-est - sud-ouest pour mieux mailler le réseau de transports en commun d'Île-de-France.</p>
<p>Au nord, il incluerait une nouvelle liaison ferroviaire Senlis - Parc Astérix - Aéroport de Roissy - Paris. à l'est il desservirait Claye-Souilly, Meaux, et incluerait une nouvelle liaison Coulommiers - Chessy TGV - Paris.</p>
<p>L'entrée dans Paris se ferait par la porte des Lilas et permettrait d'ajouter deux gare dans une partie de l'est de Paris qui est actuellement à l'écart du réseau de RER. Ensuite le RER G rejoindrait la gare de Lyon qu'il desservirait par une extension de cette gare à l'est. Ensuite entre gare de Lyon et gare Montparnasse le RER G partagerait ses voies avec la liaison TGV/grandes-lignes Montparnasse - gare de Lyon - gare du Nord en suivant un tracé globalement parallèle à la ligne 6 puis à la ligne 4 du métro.<br/> Après montparnasse le RER G rejoindrait la rue de la Convention pour ensuite la remonter jusqu'à la gare de Balard, point de sortie de Paris et nouveau pôle de transport pour desservir le 15e arrondissement et le nord d'Issy-le Moulineaux, en correspondance avec les tramways T2 et T3 et plus de la ligne 8 et de la Petite Ceinture Express.</p>
<p>Après être sorti de Paris le RER G desservirait le pôle de correspondances d'Issy-Ville puis se séparerait en deux branches.<br/> La branche ouest traverserait la forêt deMeudon dans un tunnel parallèle à celui de la ligne C pour rejoindre Viroflay-Rive-Gauche et reprendre à partir de cette gare une partie de la desserte actuellement intégrée au transilien N.</p>
<p>Le branche sud monterait vers Vélizy et longerait la nationale 118 de Vélizy juqu'aux Ulis desservant Le pôle de Vélizy 2 et le plateau de Saclay. Ensuite le tracé partirait vers l'ouest pour suivre l'ancienne ligne d'Ouest-Ceinture à Chartres jusquà Saint-Arnoult-en-Yvelines. En plus une ligne nouvelle se détacherait de la branche principale sud pour desservir le campus de Palaiseau et partir vers la Ferté-Alais ce qui permettra de mieux relier l'Essonne au pôle technologique d'Orsay - Gif - Saclay</p>
