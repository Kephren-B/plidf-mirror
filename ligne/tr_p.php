<h1 id="top">Transilien P</h1>
<p><em>Paris-Est - La Ferté-Milon / Château-Thierry / La Ferté Gaucher / Provins </em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>Le transilien P est une ligne qui dessert la banlieue est de Paris à partie de la gare de l'Est. Il serait profondément remanié suite au prolongement du RER E à Meaux et Marles-en-Brie et à la création du <a href="rer_g.php">RER G</a>. Une nouvelle desserte Meaux - Coulommiers serait créée par extension de la petite ligne de Crécy-la-Chapelle de Crécy à Tigeaux.</p>
