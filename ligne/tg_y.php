<h1 id="top">Tangentielle Yvelines</h1>
<p><em>Cergy-le-Haut - Massy-Palaiseau</em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>La tangentielle Yvelines est un nouvel axe de transport qui reliera les poles de <a href="..\pole\14c153l-cergy-pontoise.php">Cergy-Pontoise</a> et de <a href="..\pole\a75iii-massy-palaiseau.php">Massy-Palaiseau</a>. Elle reprendra l'infrastructure de la ligne de la Grande Ceinture sur la plus grande partie de son tracé. Elle permettra à de nombreux voyageurs de transiter dans l'ouest de l'Île-de-France sans avoir à passer par Paris.</p>
<p>De nombreuses correspondances permettront aux voyageurs qui l'emprunteront de se rendre à l'aéroport de Roissy, la Défense, Mantes-la-Jolie ou Saint-Quentin-en-Yvelines. À ses deux extremités elles sera en correspondance d'autres tangentielles: <a href="tg_vo.php">tangentielle Val-d'Oise</a> à <a href="..\gare\eggnif-conflans-fin-doise.php">Conflans-Fin-d'Oise</a> et tangentielles <a href="tg_ess.php">Essonnes</a> et <a href="tg_vdm.php">Val-de-Marne</a> à <a href="..\gare\k7a4hf-massy-palaiseau.php">Massy - Palaiseau</a>.</p>
