<h1 id="top">Métro 7</h1>
<p><em>(Gonesse) La Courneuve - Villejuif-Louis Aragon (Gare de Wissous/Gare d'Ablon) /  Mairie d'Ivry (Ormesson) </em></p>
<div>
<a class="section-link top-link" href="#projects-section">↓ Aller aux projets d'extension</a>
</div>
<div>
<p>La ligne 7 du métro francilien est une ligne de métro qui traverse Paris suivant un axe nord-sud. En raison de son tracé sinueux elle est relativement moins fréquentée que le reste des lignes du métro bien qu'elle desserve des pôles importants de Paris.</p>
<p>Le Plan Ile-de-France propose de l'étendre au nord et au sud pour mieux relier la Banlieue à Paris et obtenir un report du trafic depuis la nationale 2, la nationale 7 et la nationale 19 vers les transports en commun.</p>
<p>Au nord la ligne serait étendue vers Gonesse via le musée de l'air et de l'espace en correspondance avec le RER B et la tangentielle Seine-Saint-Denis au Bourget, la ligne 16 au musée de l'air et la ligne 17 à Gonesse.</p>
<p>Au sud, la ligne serait étendue pour améliorer la desserte de Wissous et de la Ville d'Orly. La branche mairie d'Ivry serait étendue elle jusqu'à Ormesson et éventuellement débranchée du tronc principal à la porte d'Italie pour être prolongée vers la Cité Universitaire et la porte d'Orléans.</p>
