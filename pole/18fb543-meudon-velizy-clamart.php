<h1>Meudon-Vélizy-Clamart</h1>
<p> <em>Communes de <span class="commune-name-span">Vélizy-Villacoublay</span>, <span class="commune-name-span">Meudon</span>, <span class="commune-name-span">Clamart</span>, <span class="commune-name-span">Châtenay-Malabry</span></em> </p>
<p id="description"> <img class="line-logo line-logo-network line-logo-network-rer" height="25" src="..\medias\logos\logo_rer.svg" title="RER" width="25"/><a class="line-link" href="..\ligne\rer_b.php"><img class="line-logo line-logo-line line-logo-line-rer-b" height="25" src="..\medias\logos\rer_B.svg" title="RER B" width="25"/></a><a class="line-link" href="..\ligne\rer_g.php"><img class="line-logo line-logo-line line-logo-line-rer-g" height="25" src="..\medias\logos\rer_G.svg" title="RER G" width="25"/></a> <img class="line-logo line-logo-network line-logo-network-m" height="25" src="..\medias\logos\logo_m.svg" title="Métro" width="25"/><a class="line-link" href="..\ligne\m_8.php"><img class="line-logo line-logo-line line-logo-line-m-8" height="25" src="..\medias\logos\m_8.svg" title="Métro 8" width="25"/></a><a class="line-link" href="..\ligne\m_9.php"><img class="line-logo line-logo-line line-logo-line-m-9" height="25" src="..\medias\logos\m_9.svg" title="Métro 9" width="25"/></a><a class="line-link" href="..\ligne\m_11.php"><img class="line-logo line-logo-line line-logo-line-m-11" height="25" src="..\medias\logos\m_11.svg" title="Métro 11" width="25"/></a> <img class="line-logo line-logo-network line-logo-network-t" height="25" src="..\medias\logos\logo_tram.svg" title="Tramway" width="25"/><a class="line-link" href="..\ligne\t_6.php"><img class="line-logo line-logo-line line-logo-line-t-6" height="25" src="..\medias\logos\tram_6.svg" title="Tramway T6" width="25"/></a> </p>
<p>Le pole Meudon-Vélizy-Clamart dont le centre est le centre commercial Vélizy 2 est un bassin d'emploi tertiaire de la banlieue sud-ouest de Paris. Situé au sommet de la butte de Vélizy il est actuellement très mal relié à Paris et au reste de l'Ile-de-France par les transports en commun mais très accessible par en voiture par l'autoroute A86 et la nationale 118 qui sont régulièrement saturées.</p>
<p>La création du tramway T6 a apporté une grande amélioration mais elle reste insuffisante comme en témoigne la saturation de la nationale 118.</p>
<p>Pour pallier à ce problème de desserte, le Plan Ile-de-France propose la création du RER G pour soulager la N118 et l'extension du RER B vers Saint-Quentin-en-Yvelines pour soulager l'autoroute A86. Ces deux projets complétés par l'extension des lignes 8, 9 et 11 apporteront une solution définitive au problème de l'accessiblité de ce secteur par les transports en commun.</p>
<p>Le projet du Grand Orlyval permettra aux habitants de se rendre très simplement à l'aéroport d'Orly.</p>
<p>En complément, le Plan Ile-de-France propose que la N118 soit enfouie avec la création du RER G ce qui permettrait de restaurer la continuité de la forêt de Meudon et supprimer la descente dangereuse de Sèvres.</p>
